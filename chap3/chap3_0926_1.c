#include <stdio.h>

int main()
{
    int i = 1, j = 4, k;
    
    k = i << 6; // 0000 0000 0000 0001 => 0000 0000 0100 0000 == 64
    printf ("%d\n", k);
    
    k = j >> 2; // 0000 0000 0000 0100 => 0000 0000 0000 0001 == 1
    printf ("%d\n", k);
    
    k = 1 << 2; // 0000 0000 0000 0001 => 0000 0000 0000 0100 == 4
    printf ("%d\n", k);
    
    k = 4096 >> 2; // 0000 1000 0000 0000 => 0000 0010 0000 0000 == 1024
    printf ("%d\n", k);
    
    return 0;
}

